function formatAiAnswer(text) {
  return String(text)
    .replace(/[&<>"']/g, (char) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#039;" }[char]))
    .replace(/\n/g, "<br>");
}

