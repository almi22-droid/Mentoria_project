function showView(view, push = true) {
  let valid = $(`[data-view="${view}"]`) ? view : "home";
  if (valid === "admin" && !isAdmin()) {
    showToast(state.account ? t("adminOnly") : t("loginRequired"));
    valid = state.account ? "dashboard" : "account";
    if (location.hash === "#admin") history.replaceState(null, "", `#${valid}`);
  }
  state.currentView = valid;
  document.body.dataset.currentView = valid;
  $$(".view-screen").forEach((screen) => screen.classList.toggle("active", screen.dataset.view === valid));
  $$("[data-route]").forEach((link) => link.classList.toggle("active", link.dataset.route === valid));
  if (push) history.pushState(null, "", `#${valid}`);
  updateMarsulaMini(valid);
  window.scrollTo({ top: 0, behavior: "smooth" });
}

