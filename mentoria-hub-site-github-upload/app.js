async function handleChat(event) {
  event.preventDefault();
  const input = $("#chatInput");
  const text = input.value.trim();
  if (!text) return;
  addMessage(state.lang === "en" ? "You" : state.lang === "ru" ? "Вы" : "Сіз", text, true);
  input.value = "";
  const key = $("#aiKey").value.trim() || localStorage.getItem("mentoriaAiKey") || "";
  const model = $("#aiModel").value.trim() || localStorage.getItem("mentoriaAiModel") || "gemini-flash-latest";
  if (key) {
    addMessage("Marsula AI", "Thinking...");
    try {
      const prompt = `You are Marsula AI, the friendly motivational shark mascot inside Mentoria Hub. You are not a generic chatbot: answer as a real study/admissions assistant for high school students. Use the student's profile, saved colleges, saved opportunities, course progress, SAT/IELTS needs, and roadmap. Be concrete: give steps, deadlines, examples, and what to click next on the platform. Reply in the user's language when possible. Keep it warm, motivating, and useful.\n\nStudent profile: ${JSON.stringify(profileContext())}\nStudent question: ${text}`;
      const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "X-goog-api-key": key
        },
        body: JSON.stringify({
          contents: [{ parts: [{ text: prompt }] }]
        })
      });
      if (!response.ok) throw new Error(`API ${response.status}`);
      const data = await response.json();
      const answer = data.candidates?.[0]?.content?.parts?.map((part) => part.text).join("") || fallbackMentorReply(text);
      $(".chat-log .message:last-child").innerHTML = `<strong>Marsula AI</strong><br>${formatAiAnswer(answer)}`;
      showMarsula("heart");
      return;
    } catch (error) {
      $(".chat-log .message:last-child").innerHTML = `<strong>Marsula AI</strong><br>${formatAiAnswer(fallbackMentorReply(text))}<br><small>Gemini fallback: ${error.message}</small>`;
      return;
    }
  }
  window.setTimeout(() => addMessage("Marsula AI", fallbackMentorReply(text)), 250);
}

