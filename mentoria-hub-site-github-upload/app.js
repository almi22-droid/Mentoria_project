function collegeStats(item) {
  const usPlaces = ["Cambridge, MA", "Stanford, CA", "Philadelphia, PA", "Berkeley, CA", "Ithaca, NY", "Chicago, IL", "Princeton, NJ", "New Haven, CT", "New York, NY", "Ann Arbor, MI"];
  const countryPlaces = {
    "United Kingdom": ["London, UK", "Oxford, UK", "Cambridge, UK", "Edinburgh, UK", "Manchester, UK"],
    Australia: ["Melbourne, AU", "Sydney, AU", "Canberra, AU", "Brisbane, AU"],
    Canada: ["Toronto, CA", "Montreal, CA", "Vancouver, CA"],
    Singapore: ["Singapore"],
    Switzerland: ["Zurich, CH", "Lausanne, CH"],
    Germany: ["Munich, DE", "Berlin, DE"],
    France: ["Paris, FR", "Lyon, FR"],
    Japan: ["Tokyo, JP", "Kyoto, JP"],
    "China (Mainland)": ["Beijing, CN", "Shanghai, CN", "Hangzhou, CN"],
    "Hong Kong SAR, China": ["Hong Kong"],
    "Republic of Korea": ["Seoul, KR"],
    Netherlands: ["Delft, NL", "Amsterdam, NL"],
    "Saudi Arabia": ["Riyadh, SA"]
  };
  const locations = item.country === "United States of America" ? usPlaces : (countryPlaces[item.country] || [item.country]);
  const seed = item.name.length + item.rank;
  const acceptance = Math.max(4, Math.min(82, Math.round(5 + item.rank * 0.38 + (seed % 9))));
  const sat = Math.max(980, Math.min(1580, Math.round((1540 - item.rank * 1.9 + (seed % 6) * 10) / 10) * 10));
  return {
    location: locations[seed % locations.length],
    type: item.country === "United States of America" ? (item.rank % 4 === 0 ? "Public" : "Private Nonprofit") : "International",
    acceptance,
    sat
  };
}

