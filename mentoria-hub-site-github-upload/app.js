function collegeLogoText(name) {
  const words = name.replace(/\([^)]*\)/g, "").split(/\s+/).filter((word) => !["the", "of", "and", "university"].includes(word.toLowerCase()));
  return (words[0]?.[0] || "U").toUpperCase();
}

