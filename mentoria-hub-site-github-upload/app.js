function finishAssessment() {
  if (!state.account) {
    showToast(t("loginRequired"));
    showView("account");
    return;
  }
  if (isAdmin()) {
    showToast(t("adminOnly"));
    showView("admin");
    return;
  }
  const answers = assessmentQuestions.map((_, index) => {
    const checked = $(`input[name="q${index}"]:checked`);
    return checked ? Number(checked.value) : null;
  });
  if (answers.some((value) => value === null)) {
    showToast("Answer all 20 questions first.");
    return;
  }
  const scores = {};
  answers.forEach((value, index) => {
    const area = assessmentQuestions[index][1];
    scores[area] = (scores[area] || 0) + value;
  });
  const ranked = Object.entries(scores).sort((a, b) => b[1] - a[1]);
  const major = majorFromArea(ranked[0][0]);
  state.assessment = { completed: true, answers, scores, recommendedMajor: major, topAreas: ranked.slice(0, 4).map(([area]) => area) };
  state.profile = { ...state.profile, major, skills: state.assessment.topAreas.join(", ") };
  state.selectedInterests = new Set([...state.selectedInterests, ...state.assessment.topAreas.filter((area) => interests.includes(area))]);
  state.selectedGoals = new Set([...state.selectedGoals, ...state.assessment.topAreas.filter((area) => goals.includes(area))]);
  earnXP(120, "+120 XP orientation completed");
  renderAll();
  saveSession();
  showMarsula("plan");
  openMarsulaVideo("plan");
}

